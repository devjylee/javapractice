package ch04;

public class IfElse {

	public static void main(String[] args) {
		var i = 100;
		if(i > 50) {
		 System.out.println("변수 i는 50보다 크다.");
		} else if(i > 30) {
		 System.out.println("변수 i는 30보다 크고 50보다 작다.");
		} else {
		 System.out.println("변수 i는 30보다 작다.");
		}
		//변수 i는 50보다 크다.
	}

}
